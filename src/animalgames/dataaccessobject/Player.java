package animalgames.dataaccessobject;

public class Player {
	private int id;
	private String name;
	
	// 获取运动员ID
	public int getId() {
		return id;
	}
	// 设置运动员ID
	public void setId(int id) {
		this.id = id;
	}
	// 获取运动员名字
	public String getName() {
		return name;
	}
	// 设置运动员名字
	public void setName(String name) {
		this.name = name;
	}
}
