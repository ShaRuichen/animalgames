package animalgames.mvc;

public class Animal {
	private int id;
	private String name;
	
	// 获取动物ID
	public int getId() {
		return id;
	}
	// 设置动物ID
	public void setId(int id) {
		this.id = id;
	}
	// 获取动物名
	public String getName() {
		return name;
	}
	// 设置动物名
	public void setName(String name) {
		this.name = name;
	}
}
