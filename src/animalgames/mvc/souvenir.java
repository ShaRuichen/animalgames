package animalgames.mvc;

public class souvenir {
	private int id;
	private String name;
	
	// 获取纪念品ID
	public int getId() {
		return id;
	}
	// 设置纪念品ID
	public void setId(int id) {
		this.id = id;
	}
	// 获取纪念品名
	public String getName() {
		return name;
	}
	// 设置纪念品名
	public void setName(String name) {
		this.name = name;
	}
}
